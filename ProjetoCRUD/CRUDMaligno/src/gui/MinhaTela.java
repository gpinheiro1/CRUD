          String nome = txtNomeMateria.getText();
          
          DefaultTableModel dtm = (DefaultTableModel) tabela.getModel();
          Object[] dados = {codigo, nome};
          dtm.addRow(dados);
          
          controller.salvarMateria(codigo, nome);
